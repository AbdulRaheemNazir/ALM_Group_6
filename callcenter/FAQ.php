            <h2>Common Questions</h2>

            <h3>1. How do I register for a library account?</h3>
            <p>To register for a library account, click on the 'Register' button on the homepage and fill out the required fields including your name, email, password, and date of birth.</p>

            <h3>2. How can I borrow or reserve media?</h3>
            <p>Once you are logged in, you can search for available media (books, journals, DVDs, etc.). If a media item is available, you will see a 'Borrow' or 'Reserve' button, which you can click to complete the action.</p>

            <h3>3. What should I do if the media I want is currently unavailable?</h3>
            <p>If the media you want is already borrowed, you can reserve it, and you will be notified when it becomes available. You can track your reservations from your dashboard.</p>

            <h3>4. How do I check my borrowing history?</h3>
            <p>In your dashboard, you will find a section called 'Borrowing History,' which lists all previously borrowed media along with due dates and return statuses.</p>

            <h3>5. How can I update my personal information?</h3>
            <p>Go to the 'Profile' section in your dashboard, where you can update personal details like your phone number, address, and email.</p>

            <h3>6. What happens if I return media late?</h3>
            <p>If you return media late, you may incur overdue fines. You can check and pay any fines from your 'Account Status' page in your dashboard.</p>

            <h3>7. How do I pay my fines or membership fees?</h3>
            <p>To pay fines or renew your membership, go to the 'Payments and Fines' page in your account. You can choose from multiple payment options, including credit card and PayPal.</p>

            <h3>8. What if I forget my password?</h3>
            <p>If you forget your password, you can click the 'Forgot Password?' link on the login page. You will be prompted to enter your email to receive password reset instructions.</p>

            <h3>9. How do I contact support if I have an issue?</h3>
            <p>If you need help, you can use the 'Contact Us' page to submit a query, or call the library's support line where a Call Center Operator will assist you.</p>

            <h3>10. Can I borrow media over the phone?</h3>
            <p>Currently, media borrowing over the phone is not supported. However, you can call the library to inquire about media availability or other services.</p>

            <h3>11. What are the operating hours of the library?</h3>
            <p>The library branches are generally open from 8:30 AM to 5:30 PM, Monday through Saturday, with a half-day during the week. Online services are available 24/7.</p>

            <h3>12. Can I borrow media from another branch?</h3>
            <p>Yes, you can request a transfer of media from another branch if it's available. This can be done from your dashboard, and you will be notified when it's ready for pick-up.</p>

            <h3>13. How do I know when my reserved media is available?</h3>
            <p>You will receive a notification via your preferred communication method (Email/SMS) when your reserved media is available for pick-up.</p>

            <h3>14. How many media items can I borrow at a time?</h3>
            <p>The number of media items you can borrow at one time depends on your membership status. You can view your borrowing limits from your account page.</p>

